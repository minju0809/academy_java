
package DBPKG.money;

import java.sql.Date;

public class MoneyVO {
	private int custno;
	private String custname;
	private String grade;
	private int sumMoney;
	private int salenol;
	private String pcode;
	private int pcost;
	private int amount;
	private int price;
	private Date sdate;

	public int getCustno() {
		return custno;
	}

	public void setCustno(int custno) {
		this.custno = custno;
	}

	public String getCustname() {
		return custname;
	}

	public void setCustname(String custname) {
		this.custname = custname;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public int getSumMoney() {
		return sumMoney;
	}

	public void setSumMoney(int sumMoney) {
		this.sumMoney = sumMoney;
	}

	public int getSalenol() {
		return salenol;
	}

	public void setSalenol(int salenol) {
		this.salenol = salenol;
	}

	public String getPcode() {
		return pcode;
	}

	public void setPcode(String pcode) {
		this.pcode = pcode;
	}

	public int getPcost() {
		return pcost;
	}

	public void setPcost(int pcost) {
		this.pcost = pcost;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public Date getSdate() {
		return sdate;
	}

	public void setSdate(Date sdate) {
		this.sdate = sdate;
	}

	@Override
	public String toString() {
		return "MoneyVO [custno=" + custno + ", custname=" + custname + ", grade=" + grade + ", sumMoney=" + sumMoney
				+ ", salenol=" + salenol + ", pcode=" + pcode + ", pcost=" + pcost + ", amount=" + amount + ", price="
				+ price + ", sdate=" + sdate + "]";
	}

}
