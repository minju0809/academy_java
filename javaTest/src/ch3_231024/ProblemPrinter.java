public class ProblemPrinter {
	int i;
	boolean b;
	double d;
	String s;

	void println(int i) {
		this.i = i;
		System.out.println(i);
	}

	void println(boolean b) {
		this.b = b;
		System.out.println(b);
	}

	void println(double d) {
		this.d = d;
		System.out.println(d);
	}

	void println(String s) {
		this.s = s;
		System.out.println(s);
	}

}
